import math

def calc_pm_evaporation(
    temp_c: float,          # температура воздуха, °C
    wind_speed: float,      # скорость ветра на высоте 2 м, м/с
    radiation: float,       # солнечная радиация, Вт/м²
    relative_humidity: float, # относительная влажность, %
    pressure_pa: float,     # атмосферное давление, Па
    z0m: float = 0.015,     # шероховатость для импульса, м
    d: float = 0.1,         # высота смещения, м
    r_s: float = 100.0      # поверхностное сопротивление, с/м
) -> float:
    """Возвращает испарение за 1 час (мм) по уравнению Пенмана-Монтейта (FAO-56)."""
    # Константы
    LAMBDA = 2.45          # скрытая теплота парообразования, МДж/кг
    CP = 0.001013          # удельная теплоёмкость воздуха, МДж/(кг·°C)
    RHO_A = 1.225          # плотность воздуха, кг/м³

    # Переводим радиацию из Вт/м² в МДж/м²·ч (1 Вт/м² = 0.0036 МДж/м²·ч)
    R_n = radiation * 0.0036  # доступная энергия (G принимаем = 0)

    # Давление насыщенного пара (es) и фактическое (ea), кПа
    es = 0.6108 * math.exp((17.27 * temp_c) / (temp_c + 237.3))
    ea = es * relative_humidity / 100.0
    vpd = es - ea  # дефицит давления пара, кПа

    # Наклон кривой давления насыщенного пара (Δ), кПа/°C
    delta = 4098 * es / (temp_c + 237.3) ** 2

    # Психрометрическая константа (γ), кПа/°C
    gamma = 0.000665 * pressure_pa / 1000  # давление в кПа

    # Аэродинамическая проводимость (g_a), м/с
    if z0m <= 0:
        z0m = 0.001
    # Безопасное вычисление: если (2 - d) <= 0, устанавливаем очень маленькую проводимость
    if (2 - d) <= 0 or wind_speed <= 0:
        g_a = 0.001
    else:
        g_a = 0.168 * wind_speed / (math.log((2 - d) / z0m) ** 2)

    # Уравнение Пенмана-Монтейта
    numerator = (delta * R_n) + (RHO_A * CP * vpd * g_a)
    denominator = LAMBDA * (delta + gamma * (1 + g_a * r_s))
    if denominator == 0:
        return 0.0
    evaporation_mm = (numerator / denominator) * 3600  # переводим из мм/с в мм/час
    return max(0.0, evaporation_mm)